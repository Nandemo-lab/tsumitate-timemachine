// 12ページを生成
function buildMonthlyPages(): MonthlyPage[] {
  const pages: MonthlyPage[] = [];
  for (const [slug, cfg] of Object.entries(FUND_MONTHLY_CONFIG)) {
    for (const amount of MONTHLY_AMOUNTS) {
      pages.push({
        fundSlug: slug,
        fundId: cfg.fundId,
        amount,
        metaTitle: MONTHLY_META_TITLE[slug]?.[amount] ?? `${amountLabel(amount)}${slug}を積み立てていたら？`,
        metaDescription: MONTHLY_META_DESC[slug]?.[amount] ?? "",
        faqs: cfg.amountFaqs[amount],
        relatedCompareSlugs: cfg.relatedCompareSlugs,
        otherAmounts: MONTHLY_AMOUNTS.filter((a) => a !== amount) as unknown as number[],
        yearLinks: [2020, 2021, 2022, 2023, 2024],
      });
    }
  }
  return pages;
}

export const MONTHLY_PAGES = buildMonthlyPages();

export function getMonthlyPage(slug: string, amount: string): MonthlyPage | undefined {
  return MONTHLY_PAGES.find(
    (p) => p.fundSlug === slug && p.amount === Number(amount)
  );
}
