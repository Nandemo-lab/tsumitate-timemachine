/** FundId → /fund/[slug] の変換。対応ページがない場合は null */
export function getFundPageSlug(fundId: string): string | null {
  return FUND_PAGE_BY_FUND_ID.get(fundId as FundPage["fundId"])?.slug ?? null;
}
